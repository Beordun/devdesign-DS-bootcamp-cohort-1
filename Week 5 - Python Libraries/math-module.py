import math

# Constants
print(f"\nPi: {math.pi}")

# Rounding functions
print(f"Ceiling of 4.3: {math.ceil(4.3)}")  # 5
print(f"Floor of 4.3: {math.floor(4.3)}")   # 4
print(f"Rounded 4.3: {round(4.3)}")         # 4
print(f"Rounded 4.5: {round(4.5)}")         # 4 (note: Python rounds to even number if exactly halfway)
